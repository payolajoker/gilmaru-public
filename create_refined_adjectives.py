def create_final_list():
    """최종 146개 리스트 생성"""
    
    # 1. MUST_INCLUDE에서 EXCLUDE 제외
    base = [w for w in MUST_INCLUDE if w not in DEFINITELY_EXCLUDE]
    print(f"필수 포함 (부정 제외): {len(base)}개")
    
    # 2. 추가 단어 선별
    additions = [w for w in GOOD_ADDITIONS if w not in DEFINITELY_EXCLUDE and w not in base]
    print(f"추가 가능 단어: {len(additions)}개")
    
    # 3. 146개 채우기
    needed = 146 - len(base)
    print(f"추가 필요: {needed}개")
    
    final = base + additions[:needed]
    final_sorted = sorted(set(final))
    
    return final_sorted

