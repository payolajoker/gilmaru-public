def load_mecab_nouns(nng_file, nnp_file):
    """mecab 명사 파일에서 모든 명사 추출"""
    all_nouns = set()
    
    try:
        # 일반 명사
        with open(nng_file, 'r', encoding='utf-8') as f:
            reader = csv.reader(f)
            for row in reader:
                if row:
                    word = row[0].strip()
                    if 1 < len(word) <= 4:  # 2~4글자만
                        all_nouns.add(word)
        
        # 고유 명사 (선택적)
        # with open(nnp_file, 'r', encoding='utf-8') as f:
        #     reader = csv.reader(f)
        #     for row in reader:
        #         if row:
        #             word = row[0].strip()
        #             if 1 < len(word) <= 4:
        #                 all_nouns.add(word)
                        
    except Exception as e:
        print(f"Error loading mecab files: {e}")
    
    return all_nouns

def categorize_words(all_nouns):
    """단어를 카테고리별로 분류"""
    
    # 감정/상태 (wordA)
    wordA = []
    for word in all_nouns:
        if word in EMOTION_KEYWORDS:
            wordA.append(word)
    
    # 자연 (wordB)
    wordB = []
    for word in all_nouns:
        if word in NATURE_KEYWORDS:
            wordB.append(word)
    
    # 사물 (wordC)
    wordC = []
    for word in all_nouns:
        if word in OBJECT_KEYWORDS_BASE:
            wordC.append(word)
        # mecab에서 추가 수집
        elif word not in EMOTION_KEYWORDS and word not in NATURE_KEYWORDS:
            wordC.append(word)
    
    # 시간/추상 (wordD)
    wordD = []
    for word in all_nouns:
        if word in TIME_ABSTRACT_BASE:
            wordD.append(word)
    
    return wordA, wordB, wordC, wordD

