def is_suitable_adjective(word):
    """긍정적이고 사용하기 좋은 형용사인지 판단"""
    # 부정적인 어간 제외
    for neg in NEGATIVE_WORDS:
        if word.startswith(neg):
            return False
    
    # 복잡한 단어 제외
    for comp in COMPLEX_WORDS:
        if word.startswith(comp):
            return False
    
    # 너무 짧거나 긴 단어 제외
    if len(word) < 2 or len(word) > 5:
        return False
    
    # 특수문자 포함된 단어 제외
    if not word.replace(' ', '').isalpha():
        return False
    
    return True

def extract_positive_adjectives(csv_file):
    """CSV에서 긍정적인 형용사만 추출"""
    adjectives = []
    
    try:
        with open(csv_file, 'r', encoding='utf-8') as f:
            reader = csv.reader(f)
            for row in reader:
                if not row:
                    continue
                    
                word = row[0].strip()  # 첫 번째 컬럼이 단어
                
                if is_suitable_adjective(word):
                    # 다 -> 하다 형태로 변환
                    full_word = word + '다'
                    adjectives.append(full_word)
    
    except Exception as e:
        print(f"Error: {e}")
    
    return sorted(list(set(adjectives)))

