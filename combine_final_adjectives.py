def filter_unique(topik1, mecab):
    """중복 제거하고 mecab에서 topik1에 없는 것만"""
    topik1_set = set(topik1)
    return [w for w in mecab if w not in topik1_set]

def main():
    import sys
    sys.stdout.reconfigure(encoding='utf-8')
    
    print(f"TOPIK I 핵심 형용사: {len(TOPIK1_CORE)}개")
    
    # mecab에서 중복 제거
    unique_mecab = filter_unique(TOPIK1_CORE, MECAB_ADDITIONS)
    print(f"Mecab 추가 가능: {len(unique_mecab)}개")
    
    # 필요한 개수 계산
    target = 146
    needed = target - len(TOPIK1_CORE)
    print(f"추가 필요: {needed}개")
    
    # mecab에서 필요한만큼 선택
