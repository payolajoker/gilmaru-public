def is_negative(word):
    """부정적 단어인지 체크"""
    for neg in NEGATIVE_KEYWORDS:
        if neg in word:
            return True
    return False

def categorize_noun(word):
    """명사를 카테고리별로 분류"""
    if is_negative(word):
        return None
    
    # wordA: 감정/상태
    for pattern in EMOTION_PATTERNS:
        if pattern in word:
            return 'A'
    
    # wordB: 자연
    for pattern in NATURE_PATTERNS:
        if pattern in word:
            return 'B'
    
    # wordC: 사물 (가장 넓은 범위)
    for pattern in OBJECT_PATTERNS:
        if pattern in word:
            return 'C'
    
    # wordD: 시간/추상
    for pattern in TIME_ABSTRACT_PATTERNS:
        if pattern in word:
            return 'D'
    
    # 기본값: 사물로 분류
    return 'C'

def load_and_categorize(nng_file):
    """mecab 명사 로드 및 카테고리 분류"""
    categories = {
        'A': [],
        'B': [],
        'C': [],
        'D': []
    }
    
    try:
        with open(nng_file, 'r', encoding='utf-8') as f:
            reader = csv.reader(f)
            for row in reader:
                if not row:
                    continue
                
                word = row[0].strip()
                
                # 필터링: 2~4글자만
                if len(word) < 2 or len(word) > 4:
                    continue
                
                # 카테고리 분류
                cat = categorize_noun(word)
                if cat:
                    categories[cat].append(word)
    
    except Exception as e:
        print(f"Error: {e}")
    
    return categories

def sample_words(categories, target_counts):
    """각 카테고리에서 필요한 개수만큼 샘플링"""
    result = {}
    
    for cat, target in target_counts.items():
        words = list(set(categories[cat]))  # 중복 제거
        random.shuffle(words)  # 무작위 섞기
        
        if len(words) >= target:
            result[cat] = sorted(words[:target])
        else:
            # 부족한 경우 일단 있는 것만
            result[cat] = sorted(words)
            print(f"⚠️ wordminus{cat}: {len(words)}개만 확보 (목표: {target}개)")
    
    return result

